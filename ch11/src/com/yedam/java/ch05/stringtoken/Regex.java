package com.yedam.java.ch05.stringtoken;

public class Regex {
	/*
	 * [] 한개의 문자
	 * 		1) [abc] -> a or b or c
	 * 		2) [^abc] -> a,b,c 이외의 하나의 문자
	 * 		3) [a-zA-Z] -> a~z A~Z 중 하나
	 * 
	 *  \d 한개의 숫자로 [0-9]와 동일
	 *  \s 공백
	 *  \w 한개의 알파벳 또는 한개의 숫자, [a-zA-Z0-9]와 동일
	 *  ? 없음 또는 한개
	 *  * 없음 또는 한개 이상
	 *  + 한개이상
	 *  {n} 정확히 n개
	 *  {n,} 최소 n개 이상
	 *  {n,m} n개에서  m개 까지
	 *  () 그룹핑 
	 */
}
